'''

햄버거 #1132

문제

오일러는 시간이 없어서 오늘 점심은 햄버거로 하기로 하였다. 햄버거를 사기 위해서 가진 돈이 모자를 경우 은행에서 돈을 찾으려고 한다. 햄버거 한 개의 가격이 K, 사려고 하는 햄버거의 개수가 N개, 현재 가진 돈의 액수가 M원이라고 할 때 여러분은 오일러가 은행에서 찾아야 하는 모자란 돈의 액수를 알려주어야 한다.

예를 들어서, 만일 햄버거 한 개의 가격이 500원이고, 사려고 하는 햄버거의 개수가 5개, 현재 오일러가 가진 돈이 2,000원이라고 할 때, 오일러가 은행에서 찾아야 하는 돈은 500원이다.
햄버거 한 개의 가격, 사려고 하는 햄버거의 개수와 오일러가 가진 돈의 액수가 주어질 때, 오일러가 은행에서 찾아야 하는 돈은 얼마인지 구하는 프로그램을 작성하여라.

입력형식

첫째 줄에는 햄버거 한 개의 가격 K, 사려고 하는 햄버거의 개수 N, 현재 오일러가 가진 돈 M이 각각 한 개의 공백을 사이에 두고 주어진다. 단, K, N은 1,000 이하의 양의 정수이고, M은 10만 이하의 양의 정수이다. (1≤K,N≤1,000, 1≤M≤100,000)

출력형식

첫째 줄에 오일러가 은행에서 찾아야 하는 돈의 액수를 출력하여라.'''

from re import M


k, n, m = map(int, input().split())
withdraw = (k*n)-m
print(withdraw)

#현재 가지고 있는 돈이 더 많을 경우 또는 동일할 경우가 있으니 아래로 수정

k, n, m = map(int, input().split())
withdraw = (k*n)-m

if k*n > m:
    print(withdraw)
else:
    print(0)


